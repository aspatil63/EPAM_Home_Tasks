function basic_operations(operation, value1, value2)
{
  //write ur code here
  if(operation == '+'){
    return value1+value2
  }

  else if(operation == '-'){
      return value1-value2
  }

  else if(operation == '*'){
      return value1*value2
  }

  else if(operation == '/'){
      return value1/value2
  }
}
