function basicOp(operation, value1, value2) {
    // Pankaj Shinde
    if (operation == '+') {
        return value1 + value2;
    }
    else if (operation == '-') {
        return value1 - value2;
    }
    else if (operation == '*') {
        return value1 * value2;
    }
    else if (operation == '/') {
        return value1 / value2;
    }
}