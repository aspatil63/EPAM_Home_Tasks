function basicOp(operation, value1, value2)
{
//  Name : Pratik Kanhaiya Rathod
//  PRN : 22010885
  
  if(operation == '+'){
    return value1+value2;
  }
  else if(operation == '-'){
    return value1-value2;
  }
  else if(operation == '*'){
    return value1*value2;
  }
  else{
    return value1/value2;
  }
}